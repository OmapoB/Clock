from pyperclip import paste
from keyboard import wait, write

keyboard_buttons = {
    "q": "й",
    "w": "ц",
    "e": "у",
    "r": "к",
    "t": "е",
    "y": "н",
    "u": "г",
    "i": "ш",
    "o": "щ",
    "p": "з",
    "[": "х",
    "]": "ъ",
    "a": "ф",
    "s": "ы",
    "d": "в",
    "f": "а",
    "g": "п",
    "h": "р",
    "j": "о",
    "k": "л",
    "l": "д",
    ";": "ж",
    "'": "э",
    "z": "я",
    "x": "ч",
    "c": "с",
    "v": "м",
    "b": "и",
    "n": "т",
    "m": "ь",
    ",": "б",
    ".": "ю",
}


def fixing():
    while True:
        wait("ctrl+alt+v")
        to_fix = paste().lower()
        for i in to_fix:
            if i not in keyboard_buttons.keys():
                continue
            else:
                to_fix = to_fix.replace(i, keyboard_buttons[i])
        write(to_fix)
